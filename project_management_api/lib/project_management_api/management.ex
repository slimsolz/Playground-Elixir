  def get_user!(identifier), do: get_user_by_id_or_screen_name(identifier)

  def get_user_by_id_or_screen_name(identifier) when is_integer(identifier) do
   Repo.get(User, identifier)
  end

  def get_user_by_id_or_screen_name(identifier) when is_binary(identifier) do
    case Integer.parse(identifier) do
      {value, ""} ->
        get_user_by_id_or_screen_name(value)
      _error ->
        User
          |> where(screen_name: ^identifier)
          |> Repo.one!()
    end
  end

